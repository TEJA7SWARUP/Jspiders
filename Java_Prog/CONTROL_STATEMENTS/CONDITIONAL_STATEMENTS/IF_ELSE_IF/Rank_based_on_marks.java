import java.util.Scanner;
class Rank_based_on_marks
{
	public static void main(String[] args) 
	{
		Scanner se = new Scanner(System.in);
		System.out.println("Enter marks : ");
		int marks = se.nextInt();
		switch(marks/10)
		{
			case 10 : System.out.println("First rank");
			break;
			case 9 : System.out.println("First rank");
			break;
			case 8 : System.out.println("Second rank");
			break;
			case 7 : System.out.println("First class");
			break;
			case 6 : System.out.println("First class");
			break;
			case 5 : System.out.println("just pass");
			break;
			case 4 : System.out.println("just pass");
			break;
			case 3 : System.out.println("Fail");
			break;
			case 2 : System.out.println("Fail");
			break;
			case 1 : System.out.println("Fail");
			break;
			case 0 : System.out.println("Fail");
			break;
			default : System.out.println("Enter valid");
		}
	}

}
